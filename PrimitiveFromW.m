function prim = PrimitiveFromW(W)
%% ============================================================
% PRIMITIVEFROMW
%
% Converts conservative variables into primitive variables.
%
% INPUT
%   W(:,:,1) = rho
%   W(:,:,2) = rho*u
%   W(:,:,3) = rho*v
%   W(:,:,4) = rho*E
%
% OUTPUT
%   prim.rho
%   prim.u
%   prim.v
%   prim.p
%   prim.T
%   prim.a
%   prim.Mach
%   prim.E
%% ============================================================

gamma = 1.4;
Rgas  = 287.0;

%% ------------------------------------------------------------
% Conservative variables
%% ------------------------------------------------------------

rho  = W(:,:,1);

rhou = W(:,:,2);

rhov = W(:,:,3);

rhoE = W(:,:,4);

%% ------------------------------------------------------------
% Velocities
%% ------------------------------------------------------------

u = rhou ./ rho;

v = rhov ./ rho;

%% ------------------------------------------------------------
% Specific total energy
%% ------------------------------------------------------------

E = rhoE ./ rho;

%% ------------------------------------------------------------
% Pressure
%%
% p = (gamma-1)*(rhoE-rho*(u²+v²)/2)
%% ------------------------------------------------------------

p = (gamma-1) .* ...
    (rhoE - 0.5*rho.*(u.^2 + v.^2));

%% ------------------------------------------------------------
% Temperature
%% ------------------------------------------------------------

T = p ./ (rho*Rgas);

%% ------------------------------------------------------------
% Speed of sound
%% ------------------------------------------------------------

a = sqrt(gamma*p./rho);

%% ------------------------------------------------------------
% Mach number
%% ------------------------------------------------------------

Mach = sqrt(u.^2 + v.^2) ./ a;

%% ------------------------------------------------------------
% Store in structure
%% ------------------------------------------------------------

prim.rho  = rho;

prim.u    = u;

prim.v    = v;

prim.p    = p;

prim.T    = T;

prim.a    = a;

prim.Mach = Mach;

prim.E    = E;

end